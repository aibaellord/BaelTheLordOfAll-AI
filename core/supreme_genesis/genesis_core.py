    async def create_from_github(
        self,
        github_url: str,
        enhancement_level: str = 'transcendent'
    ) -> GenesisResult:
        """
        Analyze a GitHub repository and create an enhanced version
        that surpasses the original.
        """
        # Analyze the repository
        analysis = await self._analyze_github_repo(github_url)
        
        # Create enhanced version
        return await self.create(
            name=f"enhanced_{analysis['name']}",
            type=analysis['detected_type'],
            description=f"Enhanced version of {analysis['name']} - surpassing the original",
            requirements=analysis['capabilities'] + ['enhanced_performance', 'additional_features'],
            quality=CreationQuality.TRANSCENDENT
        )
    
    async def _analyze_github_repo(self, url: str) -> Dict[str, Any]:
        """Analyze a GitHub repository"""
        # Extract repo info from URL
        parts = url.rstrip('/').split('/')
        repo_name = parts[-1] if parts else 'unknown'
        
        return {
            'name': repo_name,
            'detected_type': GenesisType.TOOL,
            'capabilities': ['core_functionality', 'api_access'],
            'enhancement_opportunities': ['performance', 'features', 'usability']
        }
    
    async def _save_artifact(
        self,
        spec: GenesisSpec,
        code: str,
        documentation: str,
        tests: str
    ) -> str:
        """Save artifact to disk"""
        import os
        
        # Create directory
        type_dir = spec.type.name.lower()
        artifact_dir = os.path.join(self.output_base_path, type_dir, spec.name)
        os.makedirs(artifact_dir, exist_ok=True)
        
        # Save files
        code_path = os.path.join(artifact_dir, f'{spec.name}.py')
        with open(code_path, 'w') as f:
            f.write(code)
        
        doc_path = os.path.join(artifact_dir, 'README.md')
        with open(doc_path, 'w') as f:
            f.write(documentation)
        
        test_path = os.path.join(artifact_dir, f'test_{spec.name}.py')
        with open(test_path, 'w') as f:
            f.write(tests)
        
        return artifact_dir
    
    def _calculate_quality_score(self, code: str, quality: CreationQuality) -> float:
        """Calculate quality score for generated code"""
        base_scores = {
            CreationQuality.PROTOTYPE: 0.6,
            CreationQuality.PRODUCTION: 0.85,
            CreationQuality.ENTERPRISE: 0.95,
            CreationQuality.TRANSCENDENT: 1.0
        }
        return base_scores.get(quality, 0.8)
    
    def _generate_improvements(self, code: str) -> List[str]:
        """Generate self-improvement suggestions"""
        return [
            'Add more comprehensive error handling',
            'Implement caching for performance',
            'Add logging for observability',
            'Consider async optimization'
        ]
    
    async def get_creation_stats(self) -> Dict[str, Any]:
        """Get comprehensive creation statistics"""
        return {
            'total_creations': self.total_creations,
            'success_rate': self.creation_success_rate,
            'artifacts_by_type': self._count_by_type(),
            'recent_creations': self.creation_history[-10:],
            'genesis_status': 'SUPREME'
        }
    
    def _count_by_type(self) -> Dict[str, int]:
        """Count artifacts by type"""
        counts = {}
        for result in self.created_artifacts.values():
            type_name = result.artifact_type.name
            counts[type_name] = counts.get(type_name, 0) + 1
        return counts


