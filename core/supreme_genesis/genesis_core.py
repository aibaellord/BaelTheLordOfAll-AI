        if not template:
            raise ValueError(f"No template for type: {spec.type}")
        
        # Prepare template variables
        class_name = ''.join(word.capitalize() for word in spec.name.split('_'))
        function_name = spec.name.lower().replace(' ', '_').replace('-', '_')
        
